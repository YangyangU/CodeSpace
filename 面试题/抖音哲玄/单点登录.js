// SSO 流程
// 1. 用户访问系统 A：
// 用户尝试访问系统 A，系统 A 发现用户未登录。
// 系统 A 将用户重定向到认证中心（IdP）。

// 2. 用户登录认证中心：
// 用户在认证中心输入用户名和密码进行登录。
// 认证中心验证用户身份，生成一个令牌（Token）。

// 3. 认证中心返回令牌：
// 认证中心将令牌返回给系统 A。
// 系统 A 验证令牌的有效性，并允许用户访问。

// 4. 用户访问系统 B：
// 用户尝试访问系统 B，系统 B 发现用户未登录。
// 系统 B 将用户重定向到认证中心。

// 5. 认证中心验证用户已登录：
// 认证中心发现用户已经登录，直接生成一个新的令牌并返回给系统 B。
// 系统 B 验证令牌的有效性，并允许用户访问。